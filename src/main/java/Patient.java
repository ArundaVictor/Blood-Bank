
public abstract class Patient{
    public String name;
    public int id;
    public String email;
    public String condition;
    public String age;
    public String bgroup;


public String getName(){
    return name;
}

public int getId(){
    return id;
}

public String getEmail(){
    return email;
}

public String getCondition(){
    return condition;
}

public String getAge(){
    return age;
}

public String getGroup(){
    return bgroup;
}


}